#include <iostream>
using namespace std;

int main() {
    int a = 2;
    int b = 2;

    // komparasi, relation expression

    // sebanding ==
    cout << (a == b);

    // tidak sebanding !=
    cout << (a != b);

    // kurang dari
    cout << (a <= b);

    // lebih dari
    cout << (a >= b);

    cin.get();
    return 0;
}